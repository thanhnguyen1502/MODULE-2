package ss1_intruction.practice;

public class Main {
    public static void main(String[] args) {
        int i = 10;
        double d = 20.5;
        float f = 20.5f;
        boolean b = true;
        char c = 'a';
        String s = "Hà Nội";

        System.out.println("biến i kiểu số nguyên có giá trị là : " + i);
        System.out.println("biến f kiểu số thực có giá trị là: " + f);
        System.out.println("biến d kiểu số thực có giá trị là: " + d);
        System.out.println("biến b kiểu số thực có giá trị là: " + b);
        System.out.println("biến c kiểu số thực có giá trị là: " + c);
        System.out.println("biến s kiểu số thực có giá trị là: " + s);
    }
}
